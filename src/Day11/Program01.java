package Day11;

public class Program01 {

	public static void main(String[] args) {
		// Print the pattern
		/*
		 * 1
		   22
           333
           4444
           55555
		 */
		
		for(int a=1;a<=5;a++)
		{
			for(int b=1;b<=a;b++)
			{
				System.out.print(a);
			}
			System.out.println();
	}

}}
