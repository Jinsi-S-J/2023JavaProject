package Day07;

public class Program01 {

	public static void main(String[] args) {
		// Switch
		String x= "Tuesday";
		switch (x) 
		{
		case "Sunday":
			System.out.println("Day 1 is Sunday");
			break;
		case "Monday":
			System.out.println("Day 2 is Monday");
			break;
		case "Tuesday":
			System.out.println("Day 1 is Tuesday");
			break;
		case "Wednesday":
			System.out.println("Day 1 is Wednesday");
			break;
		case "Thursday":
			System.out.println("Day 1 is Thursday");
			break;
		case "Friday":
			System.out.println("Day 1 is Friday");
			break;
		case "Saturday":
			System.out.println("Day 1 is Saturday");
			break;
		default:
			System.out.println("Invalid Selection");
			break;
			
	}

}
	}
