package Day07;

public class Program03 {

	public static void main(String[] args) {
		// while 
		int x=1;
		while(x<=5)
		{
			System.out.println("Less than equal to while statements");
			x=x+1;
		}
		double y=10.5;
		while(y>=8.5)
		{
			System.out.println("Greater equal to while statements");
			y=y-1;
		}
	}

}
